# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.

filename = 'randint.txt'
import os, random
n = 2500
rand_lim = 9

with open(filename, 'w', encoding='UTF-8') as f:
       for i in range(n):
              f.write(str(random.randint(0,rand_lim)))

with open(filename, 'r', encoding='UTF-8') as f:
       longint = f.readline(n)

print (longint)

long_pos = 0
long_pos_value = longint[0:1]
max_counter = 0
current_counter = 1
prev_value = ''
for i, x in enumerate(list(longint)):
       if x == long_pos_value and x == prev_value:
              current_counter += 1
              if current_counter > max_counter:
                     long_pos = i
                     max_counter +=1
                     long_pos_value = str(x)
       else:
            current_counter = 1
       prev_value = x

print (long_pos_value, '; position=', long_pos, '; repeat: ', max_counter)
print (longint[long_pos-max_counter+1:long_pos+1])

